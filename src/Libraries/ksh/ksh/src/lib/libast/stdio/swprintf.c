#pragma prototyped

#include "stdhdr.h"

int
swprintf(wchar_t* s, size_t size, const wchar_t* fmt, ...)
{
	va_list	args;
	int	v;

	va_start(args, fmt);
	v = vswprintf(s, size, fmt, args);
	va_end(args);
	return v;
}
