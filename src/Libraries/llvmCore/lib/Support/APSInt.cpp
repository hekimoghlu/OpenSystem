#include "llvm/ADT/APSInt.h"
#include "llvm/ADT/FoldingSet.h"

using namespace llvm;

void APSInt::Profile(FoldingSetNodeID& ID) const {
  ID.AddInteger((unsigned) (IsUnsigned ? 1 : 0));
  APInt::Profile(ID);
}
