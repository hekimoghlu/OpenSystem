/*
 * strcat --
 *
 * PUBLIC: #ifndef HAVE_STRCAT
 * PUBLIC: char *strcat __P((char *, const char *));
 * PUBLIC: #endif
 */
char *
strcat(char *s, const char *append)
{
	char *save = s;

	for (; *s; ++s);
	while ((*s++ = *append++));
	return (save);
}
