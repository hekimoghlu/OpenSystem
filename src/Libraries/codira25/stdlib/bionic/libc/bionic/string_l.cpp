#include <string.h>
#include <xlocale.h>

int strcoll_l(const char* s1, const char* s2, locale_t) {
  return strcoll(s1, s2);
}

size_t strxfrm_l(char* dst, const char* src, size_t n, locale_t) {
  return strxfrm(dst, src, n);
}
