#include <sys/cdefs.h>
__FBSDID("$FreeBSD$");

#include <string.h>

int
timingsafe_bcmp(const void *b1, const void *b2, size_t n)
{
	const unsigned char *p1 = b1, *p2 = b2;
	unsigned char ret = 0;

	while (n--)
		ret |= p1[n] ^ p2[n];

	/* map zero to zero and nonzero to one */
	return (ret + 0xff) >> 8;
}

